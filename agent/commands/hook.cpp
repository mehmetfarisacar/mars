// ?????????????????????????????????????????
//  cmd_hook_install
//  lib, symbol ayrý ayrý gelir
// ?????????????????????????????????????????
std::string cmd_hook_install(const std::string& lib, const std::string& symbol) {
    if (lib.empty() || symbol.empty())
        return json_resp("error", "hook_install expects lib and symbol");

    int pid = get_attached_pid();
    if (pid <= 0)
        return json_resp("error", "no attached process");

    uint64_t addr = find_symbol_addr(pid, lib, symbol);
    if (addr == 0)
        return json_resp("error", "symbol not found: " + lib + "::" + symbol);

    int hook_id = hook_install(pid, lib, symbol, addr);
    if (hook_id < 0)
        return json_resp("error", "hook_install failed (permission? already hooked?)");

    std::ostringstream oss;
    oss << "{"
        << "\"status\":\"ok\","
        << "\"hook_id\":" << hook_id << ","
        << "\"lib\":\"" << lib << "\","
        << "\"symbol\":\"" << symbol << "\","
        << "\"addr\":\"0x" << std::hex << addr << "\""
        << "}";
    return oss.str();
}

// ?????????????????????????????????????????
//  cmd_hook_remove
//  hook_id direkt int olarak gelir
// ?????????????????????????????????????????
std::string cmd_hook_remove(int hook_id) {
    if (!hook_remove(hook_id))
        return json_resp("error", "hook not found: id=" + std::to_string(hook_id));
    return json_resp("ok", "hook removed: id=" + std::to_string(hook_id));
}

// ?????????????????????????????????????????
//  cmd_hook_list
// ?????????????????????????????????????????
std::string cmd_hook_list() {
    auto list = hook_list_all();

    std::ostringstream out;
    out << "{\"status\":\"ok\",\"count\":" << list.size() << ",\"hooks\":[";

    for (size_t i = 0; i < list.size(); i++) {
        if (i) out << ",";
        out << "{"
            << "\"id\":" << list[i].hook_id << ","
            << "\"lib\":\"" << list[i].lib << "\","
            << "\"symbol\":\"" << list[i].symbol << "\","
            << "\"addr\":\"0x" << std::hex
            << list[i].target_addr << "\""
            << "}";
    }

    out << "]}";
    return out.str();
}

